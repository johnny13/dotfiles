OUTPUT_MSG()
{
    case $1 in

        GOOD)
            echo -e "${GOODSTRING} ${2}"
            ;;

        BAD)
            echo -e "\n${BADSTRING} ${2}\n"
            ;;

        INFO)
            echo -e "${INFOSTRING} ${2}"
            ;;

        WARN)
            echo -e "${WARNSTRING} ${2}"
            ;;

        MERG)
            echo -e "${MERGSTRING} ${2}"
            ;;

        MAKE)
            echo -e "${MAKESTRING} ${2}"
            ;;

        *)
            echo -e "${INFOSTRING} ${2}"
            ;;

    esac

    echo -n "$NORMAL"
}

